print(1 > 2)  # False || 1 es mayor que 2
print(1 < 2)  # True || 1 es menor que 2
print(1 >= 1)  # True || 1 es mayor o igual que 1
print(1 <= 4)  # True || 1 es menor o igual que 4
print(2 == 2)  # True || 2 es igual a 2
print(2 != 1)  # True || 2 es diferente de 1
print("hola" == "hola")  # True || "hola" es igual a "hola"
print(2 == "2")  # False || 2 es diferente de "2"
print(2 != "2")  # True || 2 es diferente de "2"
